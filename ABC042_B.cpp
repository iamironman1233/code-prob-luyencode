#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n, l;
    cin>>n>>l;
    string a[10001];
    for(int i=1;i<=n;i++)
    {
        cin>>a[i];
    }
    for(int x= 0;x<l;x++){
        for(int i=1;i<n;i++)
        {
            for(int j =i+1;j<=n;j++)
            {
                if(a[i]>a[j])
                {
                    swap(a[i], a[j]);
                }
            }
        }
    }
    for(int i=1;i<=n;i++)
    {
        cout<<a[i];
    }
 return 0;
}
